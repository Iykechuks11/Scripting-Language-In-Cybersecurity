#!/usr/bin/perl

# Integer
$a = 20;

# Floating Number  
$b = 20.5647;  
  
# Scientific value  
$c = 123.5e-10;  
  
# Hexadecimal Number  
$d = 0xc;  
  
# Octal Number  
$e = 074;  
  
# Binary Number  
$f = 0b1010;  

print "Integer: ", $a, "\n";
print "Floating: ", $b, "\n";
print "Scientific: ", $c, "\n";
print "Hexadecimal: ", $d, "\n";
print "Octal: ", $e, "\n";
print "Binary: ", $f, "\n";